class Alumno{

  String Nombre;
  String Carrera;
  String SemestreActual;

  Alumno(String Nom,String CarreraA,String Semestre){

    this.Nombre = Nom;
    this.Carrera = CarreraA;
    this.SemestreActual = SemestreActual;

  }

  get getNombre{
    return Nombre;
  }

  get getCarreta{
    return Carrera;
  }

  get getSemestreActual{
    return SemestreActual;
  }




}